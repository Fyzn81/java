package IfElse;

public class ifelse {

	public static void main(String[] args) {
		
		//Java Control Statement
		
		//Conditional Statement (Selection Statement)
		
		//If..else
		
		int age=20;
		
		if(age>+18)
		{
			System.out.println("Eligible for vote");
		}

		else
			
		{
			System.out.println("Not Eligible for vote");
		}
	}

}
